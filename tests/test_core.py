    def test_returns_structured_results(self, mock_retry):
        import tempfile, pathlib
        import cram.search.base as base_mod
        with tempfile.TemporaryDirectory() as td:
            base_mod._query_cache = base_mod.QueryCache(pathlib.Path(td))

            call_count = [0]
            def side_effect(fn, **kwargs):
                call_count[0] += 1
                if call_count[0] == 1:
                    return self._make_esearch_response(["111", "222"]).json()
                return self._make_esummary_response(["111", "222"]).json()

