class TestRetryLogic(unittest.TestCase):

    def test_retries_on_500(self):
        from cram.provider.openrouter import with_retry
        import requests as req
        call_count = [0]
        def flaky():
            call_count[0] += 1
            if call_count[0] < 3:
                r = MagicMock()
                r.status_code = 500
                raise req.exceptions.HTTPError(response=r)
            return "ok"
        with patch("cram.provider.openrouter.time.sleep"):
            result = with_retry(flaky, max_retries=3, base_delay=0.01)
        self.assertEqual(result, "ok")
        self.assertEqual(call_count[0], 3)

    def test_retries_on_429(self):
        from cram.provider.openrouter import with_retry
        import requests as req
        call_count = [0]
        def rate_limited():
            call_count[0] += 1
            if call_count[0] < 2:
                r = MagicMock()
                r.status_code = 429
                raise req.exceptions.HTTPError(response=r)
            return "ok"
        with patch("cram.provider.openrouter.time.sleep"):
            result = with_retry(rate_limited, max_retries=3, base_delay=0.01)
        self.assertEqual(result, "ok")

    def test_no_retry_on_proxy_error(self):
        """ProxyError = domain blocked = permanent, must not retry."""
        from cram.provider.openrouter import with_retry
        import requests as req
        call_count = [0]
        def proxy_blocked():
            call_count[0] += 1
            raise req.exceptions.ProxyError("Tunnel connection failed: 403 Forbidden")
        with self.assertRaises(req.exceptions.ProxyError):
            with_retry(proxy_blocked, max_retries=3, base_delay=0.01)
        self.assertEqual(call_count[0], 1, "ProxyError must not trigger retries")

    def test_no_retry_on_tunnel_connection_error(self):
        """ConnectionError from proxy tunnel must not retry."""
        from cram.provider.openrouter import with_retry
        import requests as req
        call_count = [0]
        def tunnel_fail():
            call_count[0] += 1
            raise req.exceptions.ConnectionError("Tunnel connection failed: 403 Forbidden")
        with self.assertRaises(req.exceptions.ConnectionError):
            with_retry(tunnel_fail, max_retries=3, base_delay=0.01)
        self.assertEqual(call_count[0], 1, "Tunnel failure must not retry")

    def test_does_retry_on_plain_connection_error(self):
        """Generic ConnectionError (not proxy) should retry."""
        from cram.provider.openrouter import with_retry
        import requests as req
        call_count = [0]
        def transient():
            call_count[0] += 1
            if call_count[0] < 3:
                raise req.exceptions.ConnectionError("Connection reset by peer")
            return "ok"
        with patch("cram.provider.openrouter.time.sleep"):
            result = with_retry(transient, max_retries=3, base_delay=0.01)
        self.assertEqual(result, "ok")
        self.assertEqual(call_count[0], 3)

    def test_raises_after_max_retries(self):
        from cram.provider.openrouter import with_retry
        import requests as req
        def always_fails():
            raise req.exceptions.Timeout("always times out")
        with self.assertRaises(req.exceptions.Timeout):
            with patch("cram.provider.openrouter.time.sleep"):
                with_retry(always_fails, max_retries=2, base_delay=0.01)

    def test_no_retry_on_400(self):
        from cram.provider.openrouter import with_retry
        import requests as req
        call_count = [0]
        def bad_request():
            call_count[0] += 1
            r = MagicMock()
            r.status_code = 400
            raise req.exceptions.HTTPError(response=r)
        with self.assertRaises(req.exceptions.HTTPError):
            with_retry(bad_request, max_retries=3, base_delay=0.01)
        self.assertEqual(call_count[0], 1, "400 Bad Request must not retry")


