class TestQueryCache(unittest.TestCase):

    def setUp(self):
        self.tmpdir = tempfile.TemporaryDirectory()
        self.data_dir = pathlib.Path(self.tmpdir.name)

    def tearDown(self):
        self.tmpdir.cleanup()

    def _make_cache(self):
        from cram.search.base import QueryCache
        return QueryCache(self.data_dir)

    def test_wal_mode_enabled(self):
        from cram.search.base import _wal_conn
        cache = self._make_cache()
        conn = _wal_conn(cache.db_path)
        row  = conn.execute("PRAGMA journal_mode").fetchone()
        conn.close()
        self.assertEqual(row[0], "wal")

    def test_set_and_get(self):
        cache = self._make_cache()
        data  = [{"pmid": "123", "title": "Test paper"}]
        cache.set("PubMed", "test query", data)
        result = cache.get("PubMed", "test query")
        self.assertEqual(result, data)

    def test_miss_returns_none(self):
        cache  = self._make_cache()
        result = cache.get("PubMed", "query not in cache")
        self.assertIsNone(result)

    def test_different_sources_dont_collide(self):
        cache    = self._make_cache()
        pubmed   = [{"pmid": "1", "title": "PubMed result"}]
        epmc     = [{"pmid": "2", "title": "EuropePMC result"}]
        cache.set("PubMed",    "hepatitis", pubmed)
        cache.set("EuropePMC", "hepatitis", epmc)
        self.assertEqual(cache.get("PubMed",    "hepatitis"), pubmed)
        self.assertEqual(cache.get("EuropePMC", "hepatitis"), epmc)

    def test_overwrite_same_key(self):
        cache   = self._make_cache()
        orig    = [{"title": "v1"}]
        updated = [{"title": "v2"}]
        cache.set("PubMed", "q", orig)
        cache.set("PubMed", "q", updated)
        self.assertEqual(cache.get("PubMed", "q"), updated)

    def test_empty_results_not_cached(self):
        """Ensures failed searches don't poison cache with empty lists."""
        from cram.search.base import cached_search, QueryCache
        import cram.search.base as base_mod
        cache = self._make_cache()
        base_mod._query_cache = cache  # inject test cache

        @cached_search("TestSource")
        def always_empty(query, max_results=10):
            return []

        always_empty("some query")
        result = cache.get("TestSource", "some query")
        self.assertIsNone(result, "Empty results must NOT be cached")

    def test_parallel_writes_no_lock_error(self):
        """§1.2: 6 parallel threads must not raise 'database is locked'."""
        cache   = self._make_cache()
        errors  = []

        def writer(i):
            try:
                cache.set("Source", f"query_{i}", [{"title": f"result_{i}"}])
            except Exception as e:
                errors.append(str(e))

        threads = [threading.Thread(target=writer, args=(i,)) for i in range(12)]
        for t in threads: t.start()
        for t in threads: t.join()

        self.assertEqual(errors, [], f"WAL parallel write errors: {errors}")
        # Verify all writes landed
        for i in range(12):
            self.assertIsNotNone(cache.get("Source", f"query_{i}"))

    def test_clear_old_entries(self):
        cache = self._make_cache()
        cache.set("PubMed", "old query", [{"title": "old"}])
        # Manually backdate the entry
        conn = sqlite3.connect(str(cache.db_path))
        conn.execute("UPDATE cache SET created_at = '2020-01-01T00:00:00'")
        conn.commit()
        conn.close()
        cache.clear(older_than_days=30)
        self.assertIsNone(cache.get("PubMed", "old query"))

    def test_clear_leaves_recent_entries(self):
        cache = self._make_cache()
        cache.set("PubMed", "fresh query", [{"title": "fresh"}])
        cache.clear(older_than_days=30)
        self.assertIsNotNone(cache.get("PubMed", "fresh query"))


