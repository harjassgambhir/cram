    def test_dedup_by_doi(self):
        from cram.search.base import dedup_results
        results = [
            {"doi": "10.1/abc", "title": "First"},
            {"doi": "10.1/abc", "title": "Dup"},
            {"doi": "10.2/xyz", "title": "Other"},
        ]
        self.assertEqual(len(dedup_results(results)), 2)

    def test_dedup_by_url(self):
        from cram.search.base import dedup_results
        results = [
            {"url": "https://x.com/paper?v=1", "title": "First"},
            {"url": "https://x.com/paper?v=2", "title": "Dup"},   # same base URL
            {"url": "https://y.com/other",     "title": "Other"},
        ]
        deduped = dedup_results(results)
        self.assertEqual(len(deduped), 2)

    def test_no_key_entries_kept(self):
        from cram.search.base import dedup_results
        results = [
            {"title": "no id 1"},
            {"title": "no id 2"},
        ]
        self.assertEqual(len(dedup_results(results)), 2)

    def test_empty_input(self):
        from cram.search.base import dedup_results
        self.assertEqual(dedup_results([]), [])

    def test_mixed_key_types(self):
        from cram.search.base import dedup_results
        results = [
            {"pmid": "1"},
            {"doi": "10.1/a"},
            {"nct_id": "NCT001"},
            {"url": "https://example.com"},
            {"title": "no-id"},
            {"pmid": "1"},      # dup pmid
            {"doi": "10.1/a"},  # dup doi
        ]
