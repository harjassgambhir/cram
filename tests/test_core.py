    def _make_esummary_response(self, ids):
        m = MagicMock()
        m.status_code = 200
        result = {}
        for pmid in ids:
            result[pmid] = {
                "title": f"Test paper {pmid}",
                "authors": [{"name": "Smith J"}, {"name": "Doe A"}],
                "source": "Test Journal",
                "pubdate": "2024",
                "elocationid": f"10.1234/{pmid}",
            }
        m.json.return_value = {"result": result}
        m.raise_for_status = MagicMock()
        return m

