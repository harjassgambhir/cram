            with patch("cram.search.pubmed.requests.get") as mock_get:
                mock_get.side_effect = [
                    self._make_esearch_response(["111", "222"]),
                    self._make_esummary_response(["111", "222"]),
                ]
                # Bypass with_retry wrapping for this test
                mock_retry.side_effect = lambda fn, **kw: fn()

                from cram.search.pubmed import tool_pubmed
                # Clear cache first
                base_mod._query_cache.db_path  # just touch it
                results = tool_pubmed.__wrapped__("portal hypertension whipple", max_results=2)

            self.assertEqual(len(results), 2)
            self.assertEqual(results[0]["source"], "PubMed")
            self.assertIn("pmid", results[0])
            self.assertIn("url", results[0])
